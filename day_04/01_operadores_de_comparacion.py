# Comparaciòn de Int en igualdad
mi_bool = 25 == 10
print(f'La comparación de 25 = 10 es: {mi_bool}')

# Comparación de Strings en igualdad
mi_bool_string = 'blaco' == 'negro'
print(f'La comparación de \"blaco\" = \"negro\" es: {mi_bool_string}')

# Comparación de Int en desigualdad
mi_comparacion_int = 10 > 7
print(f'La comparación de 10 > 7 es: {mi_comparacion_int}')